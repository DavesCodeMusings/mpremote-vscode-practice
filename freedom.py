# Tongue in cheek MicroPython module to convert pesky socialist metric
# units to good old, honest to dog, freedom loving imperial units.

# Convert degrees C to degrees F, where the F stands for Freedom!
def degreesF(degreesC):
    return round(degreesC * 1.8 + 32)

# Banana for scale. Anything but metric!
def bananas(centimeters):
    return centimeters / 18
